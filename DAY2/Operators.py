#Arithmetic Operators
a= 7
b= 2

print(a+b)  # addition
print(a-b)  #substration
print(a*b) #multiplication

print(a/b)  #3.5
print(a//b) #3

print(a%b)  #1
print(a**b) #49

#Relational Operators - It always return the boolean values(True/False)
a=5
b=10

print(a>b)  # False (Greater than)
print(a<b)  #True  (Less than)
print(a==b)  # False
print(a!=b)  #True
print(a<=b)  #True
print(a>=b)  #False

#Logical Operators - It always return the boolean values(True/False)
a=True
b=False


print(a and b)  # False
print(a or b)  #True
print(not a)  #False