#Max() - Return the maximum value

print(max(10,50,42,80,70,90,100,120,40))  #120

print(max(10.5,20.5,5.5,30.5))   #30.5

#Min() - Return the minimum value

print(min(10,50,42,8,70,90,100,120,40))  #8

print(min(10.4,5.5,8.5,5.1,10.8))   #5.1